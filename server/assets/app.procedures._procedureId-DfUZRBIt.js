async function duplicateProcedure(proc, companyId, userId, nav) {
  if (!companyId) return;
  const newCode = window.prompt("New WPS code:", `${proc.code ?? "WPS"}-COPY`);
  if (!newCode) return;
  const exclude = /* @__PURE__ */ new Set([
    "id",
    "created_at",
    "updated_at",
    "approved_at",
    "approved_by",
    "reviewed_at",
    "reviewed_by",
    "submitted_for_review_at",
    "submitted_by",
    "qr_token",
    "deleted_at",
    "deleted_by"
  ]);
  const payload = { company_id: companyId, status: "Draft", revision: "Rev 0" };
  for (const [k, v] of Object.entries(proc)) {
    if (!exclude.has(k) && v != null) payload[k] = v;
  }
  payload.code = newCode;
  payload.wps_no = newCode;
  const { data, error } = await supabase.from("procedures").insert(payload).select("id").single();
  if (error) return toast.error(error.message);
  toast.success("Duplicated — opening new draft");
  nav({ to: "/app/procedures/$procedureId", params: { procedureId: data.id } });
}
